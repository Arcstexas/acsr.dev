<h1>Management</h1>
<br>
<h3>BOARD OF DIRECTORS</h3>
<p>The association is governed by its bylaws. The governing body of the association is the Board of Directors. The board comprises of minimum 3 and maximum 7 directors. The directors work on a volunteer basis and receive no remuneration. The directors perform such functions and carry out such duties as may be assigned to them by the Board.</p>

<p>The directors, elect amongst themselves, a director to be the president. The president heads the association and presides at all meetings of the Association and the Board.</p>

<p>The Board frames Rules and Regulations in accordance with the Bylaws for the management of the association, enact a code of ethics that governs the privacy policy, and oversee day-to-day operations of the association.</p>

<p>The board is responsible for appointing committees, defining their function, and providing them with guidelines for their working.</p>

<p>The Board keeps detailed and complete account of all incomes and expenses. The books of account and all other records are kept at the office of the Association.</p>

<h3>SECRETARY</h3>
<p>A Secretary manages the secretariat of the association. The association, on a contractual basis, retains the services of a person, qualified to run and manage the secretariat. The secretary is responsible for running day-to-day operations of the association, and is answerable to the board. The secretary performs such duties as the Board assigns to the office, and exercises such powers as the Board grants to the office. In any and all cases the Secretary is bound by the privacy policy of the association.</p>

<h3>COMMITTEES</h3>
<p>The major committees appointed by the Board are the Bargaining Committee, Benefits Disbursing Committee, Membership Committee, and the Compliance Committee. A director heads each committee and the committees function under the guidelines provided by the board, and are answerable to the board for their performance.</p>

<h3>ADVISORS</h3>
<p>The Board may request any person whether a member, an associate member or a non-member, to act as advisor to the Board. The Board may elect to have more than one advisor.</p>

<p>The advisor attends the meeting of the Board by invitation. For the services rendered, the board may pay the advisor an honorarium and also reimburse for any incurred expenses. Upon being invited the advisor may also attend the meeting of the General Body but shall have no vote; provided always that the advisor, if a regular member, shall have a vote by virtue of being a member of the association.</p> 

<h3>MANAGEMENT OF FUNDS</h3>
<p>The funds of the association are deposited in bank account(s) maintained in the name of the Association.</p>

<p>The Board maintains a Bank Account Signatories List comprising of three directors that is provided to the banks. Any two out of these three directors operate the bank accounts in accordance with the Bylaws.</p>

<h3>STATUTORY FILINGS</h3>
<p>Statutory reports, as required by the law, are filed with the relevant authorities, and are current. The financial statements for each year are prepared by a Certified Public Accountant. Tax returns are also prepared and filed by a Certified Public Accountant.</p>

